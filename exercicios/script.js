// tipos primitivos 

// boolean - roda o index e clica em F12, na aba console e tem o resol tado falso
var VOuF = false;
console.log(typeof{VOuF}); 

// exemplo com o numero
var numeroQualquer = 1;
console.log(typeof{numeroQualquer});

// string 
var nome = 'Marcelo';
console.log(typeof{nome});

var funcao = function() {}
console.log(typeof{funcao});

// como declarar
var variavel = 'Marcelo';
variavel = 'Marcos';
console.log(variavel);

// let é uma variavel de escopo local de bloco
let variavel2 = 'Marcelo';
console.log(variavel2);

// variavel constante, de valor fixo, não pode ser alterada
const constante = 'Marcelo';
console.log(constante);


